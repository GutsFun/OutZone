    <p>Les ennemis sont horriblement vicieux, accompagnés par des tourelles à la cadence infernale. Mais comme chez <span>Toaplan</span> ils aiment bien nous mettre la pression, vous devrez aussi traverser les niveaux assez rapidement pour ramasser des <img src="../assets/items/energy.gif" title="Energy" class="icon"> et ainsi ne pas voir votre énergie vitale tomber à 0 et perdre bêtement une vie. Une alarme assez stressante est de toute façon là pour vous le rappeler.</p>
