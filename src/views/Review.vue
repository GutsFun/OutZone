    <p class="conclusion">OutZone est un excellent jeu à la difficulté bien dosée et qui vous demandera des nerfs d'acier pour le terminer. Un grand classique, donc, qui n'a pas pris une ride.</p>
    <h3>Portage</h3>
    <p>Malgré un succès mérité, <b>OutZone</b> n'a jamais été porté sur une console de salon. Une défunte société coréenne, <span>Techron System</span>, a réalisé pour le marché des mobiles coréen une adaptation d'<span>OutZone</span>. Le jeu a également été porté sur iOS de manière assez médiocre sous la forme de deux titres. Le premier est une émulation directe d'<span>OutZone</span> avec une tentative de mapping des touches via l'écran tactile assez catastrophique. Le deuxième, renommé <span>OutZone Reloaded</span>, a subit un lifting visuel du plus mauvais effet sans parler des contrôles tactiles tout aussi catastrophique. A quand un portage arcade perfect de ce titre ? Et bien il semblerait que ce rêve devienne réalité grâce à la société <a href="https://www.mtwo.co.jp/" target="_new">M2</a> connue pour ses portages de qualité et qui a annocé récemment l'intention de porter un certains nombre de titres <span>Toaplan</span> dont <span>OutZone</span>.</p>
